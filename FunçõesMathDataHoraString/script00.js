// funções matemáticas

//SQRT  e POW (Quadrado e Potência)

//calcular a raiz quadrada de 25
console.log(Math.sqrt(25)); //5

//calcular a potência
console.log(Math.pow(7,2)); // 7² = 49
console.log(Math.pow(4,3)); // 4³ = 64
console.log(Math.pow(27,1/3)); // ³√27= 3

// funções de arrendodamento (round,ceil, floor)
console.log(Math.round(4.3)); //4
console.log(Math.round(4.7)); //5
console.log(Math.ceil(4.1)); //5
console.log(Math.floor(4.4)); //4

// numeros aleatorios (MAth.random)
console.log(Math.random()); // gerar numero entre (0 e 999)
console.log(Math.random()*1000); // gerar numero entre (0 e 999.999)

// valor absoluto
console.log(Math.abs(-10)); //10

//minimos e máximos
console.log(Math.min(1,2,3,4,5,6,7,8,9)); //1
console.log(Math.max(1,2,3,4,5,6,7,8,9)); //9



